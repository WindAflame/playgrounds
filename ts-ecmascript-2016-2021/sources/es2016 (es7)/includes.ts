// Array.prototype.includes()
// Determines whether an array contains a given element and returns either true of false.
//
// https://babeljs.io/docs/babel-plugin-transform-inline-environment-variables#options
// https://babeljs.io/docs/babel-plugin-transform-runtime

const array1 = [1, 2, 3];

